/* The ASCII art is a tree, because the
 * dependencies are in a try-like structure
 */

#include "ascii_art.h"
#include <stdio.h>

/* Prints the tree
 */
void print_ascii_art()
{
    // print each line of the tree
    printf("                             # #### ####\n");
    printf("                         ### \\""/#|### |/####\n");
    printf("                       ##\\""/#/ \\""||/##/_/##/_#\n");
    printf("                      ###  \\/""###|/ \\""/ # ###\n");
    printf("                   ##_\\""_#\\""_\\""## | #/###_/_####\n");
    printf("                   ## #### # \\"" #| /  #### ##/##\n");
    printf("                   __#_--###`  |{,###---###-~\n");
    printf("                               \\"""" }{\n");
    printf("                              }}{\n");
    printf("                               }}{\n");
    printf("                               {{}}\n");
    printf("                          , -=-~{ .-^- _\n");
    printf("                              /  `}     '\n");
    printf("                              '   {\n");
}
